<div class="sub-title">3 methods available</div>
<div class="text">
	You will be able to upload your own photos through the box to the left of the
	<a href="<?php echo Yii::app()->createUrl('photo/submit')?>">Upload</a> page.
	You can choose between three different methods of uploading:<br>
	<br>
	<b>HTML</b> - Upload up to 10 files simultaneously using the HTML form: select your files one by one, and click "Upload".<br>
	<br>
	<b>Plugin</b> - Upload up to 30 files simultaneously via Plugin: select all the files to upload, then
	click on the "Add" button or drag them from the folder on your pc. To start the upload, click on the down arrow.
	<br>
	<br>
	<b>FTP</b> - By clicking on the "FTP" tab, you can request an FTP account. You can then immediately see your credentials and
	 all the necessary information to access and upload your pictures via FTP.<br>
	<br>
	
	After the upload (which can take several minutes depending on the number of photos uploaded), photographs will be displayed in the main body of the page, ready to be edited and submitted.
</div>
