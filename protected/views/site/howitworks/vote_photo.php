<div class="sub-title">Criteria and evaluation tools</div>
<div class="text">
	Each user can assign their votes to the photograph which is displayed when the
	<a href="<?php echo Yii::app()->createUrl('photo/vote')?>">Vote</a> page opens.
	If you do not want to assign a rating to the photo selected, you can skip the photo by reloading the page.
	We ask that you take the utmost rigor in assessing the photos, to the best of your skills, in the following respects:
	<ul>
		<li>Technical (eg composition, light, white balance, noise, sharpness and focus of photography);</li>
		<li>Communicational (eg emotions evoked by the photograph, a key moment captured by the photographer);</li>
		<li>Commercial (eg marketability of photography in relation to the prices set by the user);</li>
		<li>Information, evaluating the correctness and accuracy of the information (eg title, location, category, etc..).</li>
	</ul>
	In choosing your vote, you will use the tools provided by <b> GeoStockPhoto </b>, namely:
	<ul>
		<li>Crop of the picture to 100% in order to assess focus, sharpness and noise;</li>
		<li>Histogram of photography, in order to assess exposure and noise;</li>
		<!-- <li>Comparison with the five best photos belonging to the same category, and located in the vicinity of photography put to vote.</li> -->
	</ul>
</div>

