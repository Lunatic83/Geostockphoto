<div class="sub-title">Benefits</div>
<div class="text">
	<span>When you reach one of three photographer levels (ie newbie, Pro or Power), you will increase the percentage
you earn on your sales. </span> The highest level is the Power:
you have demonstrated an ability and fidelity such that your photos will no longer be evaluated, but will automatically be offered for sale with a vote of 5.
</div>