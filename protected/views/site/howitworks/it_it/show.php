<div class="sub-title">Portfolio</div>
<div class="text">
	<b>GeoStockPhoto</b> mette a disposizione dei propri fotografi una mappa personale con tutte le proprie fotografie messe in vendita.<br>
	E' possibile raggiungere questa pagina in 2 modi:
	<ul>
		<li>tramite link presente nella pagina personale del fotografo</li>
		<li>tramite link diretto www.geostockphoto.com/portfolio/<i>username</i></li>
	</ul>
</div>
<br>
<div class="sub-title">Pagina Personale</div>
<div class="text">
	<b>GeoStockPhoto</b> mette a tua disposizione una pagina personale visibile a tutti gli utenti e visitatori del sito.
	<br>
	Una prima parte è dedicata a tutti gli utenti registrati, contenente:
	<ul>
		<li><b>Foto profilo</b></li>
		<li><b>Informazioni personali</b></li>
		<li><b>Posizione geografica</b></li>
	</ul>
	<br>
	Una seconda parte è dedicata ai soli fotografi, abilitata quindi solo per utenti con un certo numero di foto in portfolio.
	Questa seconda parte conterrà:
	<ul>
		<li><b>Statistiche del fotografo su GeoStockPhoto</b> (con almeno 10 foto in portfolio)</li>
		<li><b>Informazioni sulla carriera inserite dal fotografo</b> (con almeno 100 foto in portfolio)</li>
		<li><b>Contatti e link ad altri siti del fotografo</b> (con almeno 1000 foto in portfolio)</li>
	</ul>
</div>
