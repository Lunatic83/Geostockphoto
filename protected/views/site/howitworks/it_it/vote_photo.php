<div class="sub-title">Criteri e strumenti di valutazione</div>
<div class="text">
	Ogni utente può attribuire il proprio voto alla fotografia che trova visualizzata all’apertura della pagina
	<a href="<?php echo Yii::app()->createUrl('photo/vote')?>">Vote</a>.
	Nel caso  tu non voglia attribuire  un voto alla fotografia estratta, potrai cambiarla semplicemente aggiornando la pagina.
	Ti chiediamo  di adottare il massimo rigore nel valutare  le fotografie, al meglio delle tue competenze, sotto i seguenti profili:
	<ul>
		<li>Tecnico (e.g. composizione, luce, bilanciamento, rumore, nitidezza e messa a fuoco della fotografia);</li>
		<li>Comunicazionale (e.g. emozioni evocate dalla fotografia, momento chiave catturato dall’autore);</li>
		<li>Commerciale (e.g. vendibilità della fotografia anche in relazione ai prezzi stabiliti dall’utente);</li>
		<li>Informativo, valutandone la correttezza e precisione delle informazioni (e.g. titolo, posizione geografica, categoria, etc.).</li>
	</ul>
	Nell’attribuire il  tuo voto, utilizzerai gli strumenti messi a disposizione da <b>GeoStockPhoto</b>, e precisamente:
	<ul>
		<li>Ritaglio della fotografia al 100% al fine di valutarne messa a fuoco, nitidezza e rumore;</li>
		<li>Istogramma della fotografia, al fine di valutarne esposizione e rumore;</li>
		<!-- <li>Confronto con le 5 migliori fotografie facenti parte della stessa categoria e localizzate nelle vicinanze dalla fotografia oggetto di votazione.</li> -->
	</ul>
	Nel caso in cui la fotografia visualizzata nella pagina costituisca violazione della normativa applicabile, e/o di diritti di terzi, ti chiediamo di astenerti dall’attribuire
	un voto e segnalare la violazione a <b>GeoStockPhoto</b> attraverso il link alla pagina "Report Abuse".
</div>

