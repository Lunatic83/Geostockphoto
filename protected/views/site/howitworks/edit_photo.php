<div class="sub-title">EXIF and IPTC metadata</div>
<div class="text">
	During the upload of your photos, EXIF and IPTC metadata are read and stored to speed up the subsequent submission procedure,
by automatically compiling the title, keywords, geographical location and all the shooting information (ISO, aperture, exposure time and date).
You can edit each field, with the exception of the shooting information, before completing the submission.
</div>
<br/>

<div class="sub-title">Selection of single and multiple photos</div>
<div class="text">
	In <b> GeoStockPhoto </b> you can edit fields of more than one photo at once. The selection of the photos is done in the
<a href = "<?php echo Yii::app()->createUrl('photo/submit')?>">Upload</a> page.
By default, the single selection is active, so by clicking on a photo, only that photo is selected.
By checking the checkbox at the top of the page, you can activate the
multiple selection. Then, by clicking on the individual picture on the page, this is selected
(if it was not already) along with all previous selected photos or, conversely,
deselected (if it was previously selected).
In addition, using two buttons ("Select All" and "Deselect All"), you can select
or deselect all of the photos on the page.
As a result of each selection, the right side box is updated with the information common to all selected photos.</div>
